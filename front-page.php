<script>
// Групуємо документи
function groupDocuments(documentTypes) {
  return documentTypes.reduce((acc, doc) => {
    if (!doc.group) return acc;
    acc[doc.group] ??= [];
    acc[doc.group].push(doc);
    return acc;
  }, {});
}
// Сортуємо всередині группи
function sortByOrder(a, b) {
  return (a.order ?? 999) - (b.order ?? 999);
}
// Рендерими чекбокси
function renderDocumentCheckboxes(container, documents, groupKey) {
  documents
    .sort(sortByOrder)
    .forEach((doc, index) => {
      const id = `${groupKey}_${index}`;

      container.insertAdjacentHTML('beforeend', `
        <div class="checkbox">
          <input
            id="${id}"
            class="checkbox__input"
            type="checkbox"
            name="document-type[]"
            value="${doc.id}"
          >
          <label for="${id}" class="checkbox__label">
            <span class="checkbox__text">${doc.text}</span>
          </label>
        </div>
      `);
    });
}
	
(async () => {

  const response = await fetch(
    '/wp-admin/admin-ajax.php?action=get_calculator_config'
  );

  const result = await response.json();

  if (!result.success) {
    console.error('Config load failed', result);
    return;
  }

  const config = result.data;

  const grouped = groupDocuments(config.documentTypes);

  renderDocumentCheckboxes(
	  document.querySelector('#record_request_apostille'),
	  grouped.record_request_apostille || [],
	  'record_request'
  );

  renderDocumentCheckboxes(
	  document.querySelector('#notarization_apostille'),
	  grouped.notarization_apostille || [],
	  'notarization'
  );

})();
</script>

<script>
document.addEventListener("DOMContentLoaded", function () {

    const button = document.getElementById("check-order-status");
    const input = document.getElementById("order-number-input");
    let wasFilled = false;

    input.addEventListener("click", function () {
        if (wasFilled) {
            input.value = "";
            input.classList.remove('_form-error');
            wasFilled = false;
        }
    });

    button.addEventListener("click", function () {

        const orderNumber = input.value.trim();

        if (!orderNumber) {
            input.value = "Enter order number";
            input.classList.add('_form-error');
            wasFilled = true;
            return;
        }

        fetch(`/wp-json/orders/v1/status/${encodeURIComponent(orderNumber)}`)
            .then(res => res.json())
            .then(data => {

                if (data.status) {

                    input.classList.remove('_form-error');
                    input.value = data.textStatus;
                    wasFilled = true;
					
					if (data.tracking_url) {
						window.open(data.tracking_url, '_blank');
					}

                } else {

                    input.value = data.message || "Not found";
                    input.classList.add('_form-error');
                    wasFilled = true;
                }
            })
            .catch(() => {
                input.value = "Server error";
                input.classList.add('_form-error');
                wasFilled = true;
            });

    });

});
