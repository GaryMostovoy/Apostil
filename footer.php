<script>
document.addEventListener('DOMContentLoaded', function () {
  const vimeoPopups = document.querySelectorAll('.popup');
  
  vimeoPopups.forEach(popup => {
    const triggerSelector = `[data-popup='#${popup.id}']`;
    const lastFrame = popup.querySelector('.last-frame');
    const content = popup.querySelector('.popup__content');
    const closeBtns = popup.querySelectorAll('[data-close]');
    let player = null;
    let iframeLoaded = false;

    function showLastFrame() {
      if (lastFrame) lastFrame.style.display = 'block';
      const iframe = popup.querySelector('.vimeo-player');
      if (iframe) {
        iframe.style.visibility = 'hidden';
        iframe.style.pointerEvents = 'none';
      }
    }

    function hideLastFrame() {
      if (lastFrame) lastFrame.style.display = 'none';
      const iframe = popup.querySelector('.vimeo-player');
      if (iframe) {
        iframe.style.visibility = 'visible';
        iframe.style.pointerEvents = 'auto';
      }
    }

    async function loadVimeoAPI() {
      if (window.Vimeo) return;
      await new Promise(resolve => {
        const s = document.createElement('script');
        s.src = 'https://player.vimeo.com/api/player.js';
        s.onload = resolve;
        document.body.appendChild(s);
      });
    }

    async function initPlayer(videoId) {
      await loadVimeoAPI();
      const iframe = popup.querySelector('.vimeo-player');
      if (!iframe) return;

      // сброс предыдущего плеера
      if (player) {
        try { player.pause().catch(()=>{}); } catch(e){}
        try { player.unload().catch(()=>{}); } catch(e){}
        player = null;
      }

      iframe.src = `https://player.vimeo.com/video/${videoId}?autoplay=1&autopause=0&title=0&byline=0&portrait=0&dnt=1`;

      player = new Vimeo.Player(iframe, {
        id: videoId,
        autopause: false,
        title: 0,
        byline: 0,
        portrait: 0,
        dnt: 1
      });

      player.on('ended', showLastFrame);
      player.on('play', () => { hideLastFrame(); player.setCurrentTime(0).catch(()=>{}); });
    }

    document.querySelectorAll(triggerSelector).forEach(trigger => {
      trigger.addEventListener('click', function () {
        const videoId = this.dataset.videoId || popup.dataset.videoId;
        if (!videoId) return;
        initPlayer(videoId);
        popup.setAttribute('aria-hidden', 'false');
      });
    });

    function closePopup() {
      if (player) {
        try { player.pause().catch(()=>{}); } catch(e){}
        try { player.unload().catch(()=>{}); } catch(e){}
        player = null;
      }
      const iframe = popup.querySelector('.vimeo-player');
      if (iframe) iframe.src = '';
      showLastFrame();
      popup.setAttribute('aria-hidden', 'true');
    }

    closeBtns.forEach(btn => btn.addEventListener('click', closePopup));
    popup.addEventListener('click', e => {
      if (!content.contains(e.target)) closePopup();
    });
  });
});
